    private void cargarResultados(ListaSimple<Expediente> lista){
        String[] columnas = {"ID", "Nombres", "Apellidos", "DNI", "Asunto", "Prioridad"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);
        for (int i = 0; i < lista.tamaño(); i++) {
            Expediente e = lista.Obtener(i);
            Interesado x = e.getInteresado();
            String[] fila = {
                e.getId(),
                x.getNombres(),
                x.getApellidos(),
                x.getDni(),
                e.getAsunto(),
                e.getPrioridad()
            };
            modelo.addRow(fila);
        }
        tablaResultados.setModel(modelo);
        lblCoincidencias.setText("Coincidencias encontradas: " + lista.tamaño());
      
    }
