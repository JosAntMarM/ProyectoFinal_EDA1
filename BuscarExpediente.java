    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String texto = txtBusqueda.getText().trim().toLowerCase();
        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "⚠️ Ingresa un término para buscar.");
            return;
        }
        ListaSimple<Expediente> coincidencias = new ListaSimple<>();
        NodoDoble<Expediente> actual = gestor.getListaRegistrados().getHead();
        while(actual != null){
            Expediente e = actual.dato;
            Interesado i = e.getInteresado();
            if (e.getId().toLowerCase().contains(texto) || i.getNombres().toLowerCase().contains(texto) || i.getApellidos().toLowerCase().contains(texto)){
                coincidencias.Agregar(e);                
            }
            actual = actual.next;
        }
        cargarResultados(coincidencias);
    }//GEN-LAST:event_btnBuscarActionPerformed

