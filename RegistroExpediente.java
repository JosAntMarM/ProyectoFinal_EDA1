    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!validarCampos()) {
            return;
        }
        try{
            String nombre = txtNombre.getText().trim();
            String dni = txtDni.getText().trim();
            String telefono = txtTelefono.getText().trim();
            String apellidos = txtApellidos.getText().trim();
            String email = txtEmail.getText();
            boolean esTrabajador = chkUlima.isSelected();
            String asunto = txtAsunto.getText();
            String prioridad = (String) comboPrioridad.getSelectedItem();
            String docTitulo = txtTitulo.getText();
            String docDesc = txtDescripcion.getText();
            Interesado interesado = new Interesado(dni, nombre, apellidos, telefono, email, esTrabajador);
            Documento docRef = new Documento(docTitulo, docDesc, "archivo.pdf");
            gestor.registrarExpediente(prioridad, interesado, asunto, docRef);
            if (!validarCampos()) {
                return;
            }
           
            
            JOptionPane.showMessageDialog(this, "‚úÖ Expediente registrado con √©xito");
            limpiarCampos();
        }
        
        catch(Exception ex) {
        JOptionPane.showMessageDialog(this, "‚ùå Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private boolean validarCampos(){
        String nombre = txtNombre.getText().trim();
        String apellidos = txtApellidos.getText().trim();
        String dni = txtDni.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String email = txtEmail.getText().trim();
        String asunto = txtAsunto.getText().trim();
        String docTitulo = txtTitulo.getText().trim();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el nombre.");
            return false;
        }   
        if (apellidos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar los apellidos.");
            return false;
        }
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el DNI.");
            return false;
        }
        if (!dni.matches("\\d{8}")) {
            JOptionPane.showMessageDialog(this, "‚ùå El DNI debe tener 8 d√≠gitos.");
            return false;
        }
        if (telefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el tel√©fono.");
            return false;
        }
        if (!telefono.matches("\\d{9}")) {
            JOptionPane.showMessageDialog(this, "‚ùå El tel√©fono debe tener 9 d√≠gitos.");
            return false;
        }
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el email.");
            return false;
        }
        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, "‚ùå Ingresa un email v√°lido.");
            return false;
        }
        if (asunto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el asunto.");
            return false;
        }
        if (docTitulo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar el titulo del documento.");
            return false;
        }

        return true;
    }
    
